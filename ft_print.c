#include "ft_printf.h"

int		ft_putchar(int c)
{
	ft_putchar_fd(c, 1);
	return (1);
}

int		ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
